    private void exibeInformacoes() {
        ArrayList<Ferramentas> ferramentas = controleferramentas.retornarTodos();

        for (int i = 0; i < 200; i++) {
            jMostrandoFerramentas.setValueAt("", i, 0);
            jMostrandoFerramentas.setValueAt("", i, 1);
            jMostrandoFerramentas.setValueAt("", i, 2);
        }

        for (int i = 0; i < ferramentas.size() && ferramentas.get(i) != null; i++) {
            jMostrandoFerramentas.setValueAt(ferramentas.get(i).getFerramentaNome(), i, 0);
            jMostrandoFerramentas.setValueAt(ferramentas.get(i).getFerramentaMarca(), i, 1);
            jMostrandoFerramentas.setValueAt(ferramentas.get(i).getFerramentaValor(), i, 2);

        }
    }

